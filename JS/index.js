function  fa ( )  {
    var  escolha  =  prompt ("O que Emily deve fazer ? \n A) \n B)").toLocaleUpperCase();

    if ( escolha  ==  "A" ) {
        location.assign("../HTML/gameover.html")
    }  else  if  ( escolha  ==  "B" ) {
        location.replace ("../HTML/desbloqueio-da-fase-dois-emily.html") ;
    }  else  {
        alert ( "Por favor, se atenha as opções propostas!" ) ;
        f () ;
    }
}

